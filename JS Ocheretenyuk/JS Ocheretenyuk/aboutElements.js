function solutionTask6_9() {
    
    work = spy(work);

    work(1, 2);
    work(4, 5);

    for (let args of work.calls) {
        alert('call:' + args.join());
    }

    function work(a, b) {
        alert(a + b);
    }

    function spy(func) {
        wrapper.calls = [];
        return wrapper;
        
        function wrapper(...args) {
            wrapper.calls.push(args);
            return func.apply(this, arguments);
        }
    }
}

