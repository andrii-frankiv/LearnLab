function solutionTask6_1() {
    let n = Number(prompt("n: "));
    let array = [1,1];
    n == 1? alert(array[1]) : fib(n-2);
    
    function fib(n){
        if(n == 0) alert(array[1]);
        else{
            temp = array[0];
            array[0] = array[1];
            array[1] += temp;
            fib(n-1);
        }
    }

}

function solutionTask6_3() {
    let arr = [1, 2, 3, 4, 5, 6, 7];
    alert(arr.filter(inBetween(3, 6)));
    alert(arr.filter(inArray([1, 2, 6, 10])));

    function inBetween(from, to) {
        return function (item) {
            return item >= from && item <= to;
        }
    }

    function inArray(array) {
        return function (item) {
            return array.includes(item, 0);
        }
    }
}

